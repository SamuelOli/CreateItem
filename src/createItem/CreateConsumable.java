    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        if(txtInitialPower.getText().isEmpty()){
            txtInitialPower.setText("0");
        }
        if(txtName.getText().isEmpty() || txtStack.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        }else if(Float.parseFloat(txtInitialPower.getText()) >= 0 && Integer.parseInt(txtStack.getText()) >= 0){
                        
            Menu menu = new Menu();
            menu.newConsumable(new ItemConsumable(txtName.getText(), Float.parseFloat(txtInitialPower.getText()), Integer.parseInt(txtStack.getText())));
            exit();
        }else{
            JOptionPane.showMessageDialog(null, "Digite valores validos\nPoder Inicial recebe apenas numeros reais iguais ou maiores que zero\nQuantidade recebe apenas numeros inteiro iguais ou maiores que zero");
        }
    }//GEN-LAST:event_btnCreateActionPerformed
    public void exit(){
        Menu menu = new Menu();
        menu.setVisible(true);
        setVisible(false);
        dispose();
    }
